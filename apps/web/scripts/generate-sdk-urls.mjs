import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const sdkPath = join(__dirname, '../src/app/@generated/api/sdk.gen.ts');
const outputPath = join(__dirname, '../src/app/@generated/api/sdk-urls.gen.ts');

const sdkSource = readFileSync(sdkPath, 'utf8');
const operationPattern =
  /export const (\w+) = [^;]*?\{ url: '([^']+)'/gs;

const operations = [...sdkSource.matchAll(operationPattern)].map(
  ([, name, url]) => ({ name, url }),
);

if (operations.length === 0) {
  throw new Error(`No SDK operations found in ${sdkPath}`);
}

const imports = operations.map(({ name }) => name).join(', ');
const mapEntries = operations
  .map(({ name, url }) => `  [${name}, '${url}'],`)
  .join('\n');

const output = `// This file is auto-generated by scripts/generate-sdk-urls.mjs

import { ${imports} } from './sdk.gen';

export type SdkOperationFn = typeof ${operations[0].name}${operations.slice(1).map(({ name }) => ` | typeof ${name}`).join('')};

const sdkUrlByFn = new Map<SdkOperationFn, string>([
${mapEntries}
]);

export function getSdkUrl(fn: SdkOperationFn): string {
  const url = sdkUrlByFn.get(fn);
  if (!url) {
    throw new Error('Unknown SDK operation');
  }
  return url;
}
`;

writeFileSync(outputPath, output);
